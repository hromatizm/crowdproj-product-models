package com.product.model.validation

import chain.ICorChainDsl
import chain.worker
import com.product.model.InnerPmContext
import com.product.model.helper.errorValidation
import com.product.model.helper.fail
import com.product.model.inner.InnerPmLock

fun ICorChainDsl<InnerPmContext>.validateLockProperFormat(title: String) = worker {
    this.title = title
    val regExp = Regex("^[0-9a-zA-Z-]+$")
    on { pmValidating.lock != InnerPmLock.NONE && !pmValidating.lock.asString().matches(regExp) }
    process {
        val encodedId = pmValidating.lock.asString()
        fail(
            errorValidation(
                field = "lock",
                violationCode = "badFormat",
                description = "value $encodedId must contain only"
            )
        )
    }
}
